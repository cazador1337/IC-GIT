    private void jTableEnginesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEnginesMouseClicked
        int pos = jTabbedPaneMaster.getSelectedIndex();
        int posSel = jTableEngines.getSelectedRow();
        if (posSel == -1) {
            return;
        }
        switch (pos) {
            case 0: {
                break;
            }
            case 1: {
                break;
            }
            case 2: {
                JJFuzzy jj = managerEngines.getEngine(posSel);
                jTextAreaFCLEditor.setText(jj.getFis().toStringFcl());
                break;
            }
            case 3: {
                jLabelSelected.setText(managerEngines.getEngine(posSel).getLabel());
                break;
            }
        }
    }//GEN-LAST:event_jTableEnginesMouseClicked

