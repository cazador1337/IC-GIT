    private void gerarTodos() {
        Combinator co = new Combinator();
        LinkedList<LinkedList<Integer>> map = co.combinar(new int[]{Setting.DEFUSSIFIER_CENTER_OF_AREA, Setting.DEFUSSIFIER_CENTER_OF_GRAVITY,
            Setting.DEFUSSIFIER_CENTER_OF_GRAVITY_FUNCTIONS, Setting.DEFUSSIFIER_CENTER_OF_GRAVITY_SINGLETONS,
            Setting.DEFUSSIFIER_LEFT_MOST_MAX, Setting.DEFUSSIFIER_MEAN_MAX, Setting.DEFUSSIFIER_RIGHT_MOST_MAX},
                new int[]{Setting.RULE_ACTIVATION_MIN, Setting.RULE_ACTIVATION_PRODUCT},
                new int[]{Setting.RULE_AGGREGATION_BOUNDED_SUM, Setting.RULE_AGGREGATION_MAX,
                    Setting.RULE_AGGREGATION_NORMED_SUM, Setting.RULE_AGGREGATION_PROB_OR,
                    Setting.RULE_AGGREGATION_SUM});
        managerNewEngines.makeEngines(map,
                jTextFieldPathFileCreateEngine.getText(),
                (Double) jSpinnerFator.getValue(),
                (int) jSpinnerTermosAntecedentes.getValue(),
                jCheckBoxInvert.isSelected(),
                jProgressBarStatus);

    }
